func displayState(g *Game, guess string) {
	fmt.Print("Guessed: ")
	displayLetters(g.FoundLetters)
	fmt.Print("Used: ")
	displayLetters(g.UsedLetters)

	switch g.State {
	case GoodGuess:
		fmt.Println("Good guess!")
	case AlreadyGuess:
		fmt.Printf("Letter '%s' was already used\n", guess)
	case BadGuess:
		fmt.Printf("Bad guess, '%s' is not in the word\n", guess)
	case Lost:
		fmt.Print("You lost :(! The word was: ")
		displayLetters(g.Letters)
	case Won:
		fmt.Print("You won ! The word was: ")
		displayLetters(g.Letters)
	}

}

func displayLetters(l []string) {
	for _, c := range l {
		fmt.Printf("%v", c)
	}
	fmt.Println()
}
